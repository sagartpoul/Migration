-- CREATE TABLESPACE
-- 
CONNECT sys/director as sysdba;
CREATE USER SAHYADRI IDENTIFIED BY DIRECTOR;
GRANT DBA TO SAHYADRI;
CREATE TABLESPACE SAHYADRI DATAFILE 'E:\Oracle\SAHYADRI.DBF' SIZE 50M AUTOEXTEND ON NEXT 10M;
ALTER USER SAHYADRI DEFAULT TABLESPACE SAHYADRI;
grant read, write on directory CSV_OUTPUT_DIR to SAHYADRI;
GRANT EXECUTE ON UTL_FILE TO SAHYADRI;
-- END OF CREATE TABLESPACE


