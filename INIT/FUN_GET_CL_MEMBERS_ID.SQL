CREATE OR REPLACE FUNCTION FUN_GET_CL_MEMBERS_ID
(
	p_MEMBERS_ID NUMBER
)
RETURN NUMBER IS	
	v_NEW_MEMBERS_ID NUMBER;	
BEGIN
	BEGIN	
		IF( p_MEMBERS_ID > 1000000 ) THEN 
			v_NEW_MEMBERS_ID := p_MEMBERS_ID - 600000;			
			RETURN v_NEW_MEMBERS_ID;
		ELSE
			RETURN p_MEMBERS_ID;
		END IF;		
	END;	
END FUN_GET_CL_MEMBERS_ID;
