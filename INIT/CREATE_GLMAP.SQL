DECLARE
v_ExistCount NUMBER := 0;
BEGIN
DBMS_OUTPUT.PUT_LINE ('....START...creating GLMAP.....');
--DROP TABLE IF EXISTS
	SELECT COUNT(*) INTO v_ExistCount FROM user_tables
	WHERE TABLE_NAME = 'GLMAP';
	
	IF (v_ExistCount = 1 ) THEN
		EXECUTE IMMEDIATE 'DROP TABLE GLMAP';
	END IF;
--STEP-1--START-CREATE TABLE GLMAP
EXECUTE IMMEDIATE
'CREATE TABLE  "GLMAP" 
   (
    "BE_GLAC" NUMBER(3), 
	"CL_GLAC" VARCHAR2(8), 
	"TITLE" VARCHAR2(255), 
	"GL_GROUP" VARCHAR2(8),
	"CL_TABLE_NAME" VARCHAR2(255),
	"BE_TYPE" VARCHAR2(30), 
	"ACCATA" VARCHAR2(4), 
	"BE_ACCATA" VARCHAR2(30), 
	"BE_IAT" VARCHAR2(4), 
	"BE_ACNO_PREFIX" NUMBER(3), 
	"BE_ACNO" NUMBER(5)
   )';
   
EXECUTE IMMEDIATE 'CREATE INDEX "GLMAP_BE_IDX1" ON  "GLMAP" ("CL_GLAC")';

DBMS_OUTPUT.PUT_LINE ('....created table GLMAP.....');
--STEP-1--END-CREATE TABLE GLMAP
DBMS_OUTPUT.PUT_LINE ('....END...creating GLMAP.....');
END;
