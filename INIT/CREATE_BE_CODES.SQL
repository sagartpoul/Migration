DECLARE
v_ExistCount NUMBER := 0;
BEGIN

DBMS_OUTPUT.PUT_LINE ('....START...constructing BE_CODES.....');

--DROP TABLE IF EXISTS
	SELECT COUNT(*) INTO v_ExistCount FROM user_tables
	WHERE TABLE_NAME = 'BE_CODES';
	
	IF (v_ExistCount = 1 ) THEN
		EXECUTE IMMEDIATE 'DROP TABLE BE_CODES';
	END IF;
--STEP-1--START-CREATE TABLE BE_CODES_MAP
	EXECUTE IMMEDIATE
	'CREATE TABLE  "BE_CODES" 
	   (
		"GROUP_ID" VARCHAR2(20),
		"CLIENT_CODE" VARCHAR2(20),
		"DESCRIPTION" VARCHAR2(50),
		"BE_CODE" VARCHAR2(10),
		"REMARK" VARCHAR2(100)		
		)';
	DBMS_OUTPUT.PUT_LINE ('....created table BE_CODES.....');
	EXECUTE IMMEDIATE 'CREATE INDEX "BE_CODES_BE_IDX1" ON  "BE_CODES" ("GROUP_ID", "CLIENT_CODE", "BE_CODE")';
--STEP-1--START-CREATE TABLE BE_CODES_MAP
END;
