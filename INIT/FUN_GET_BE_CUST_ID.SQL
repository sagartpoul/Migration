FUNCTION FUN_GET_BE_CUST_ID
(
p_MEMBERS_ID NUMBER
)
RETURN VARCHAR2 IS
v_CUST_ID VARCHAR2(13);

BEGIN

	BEGIN
		
		SELECT A.CUST_ID INTO v_CUST_ID FROM CUSTOMER A 
		INNER JOIN MEMBERS B ON 
		A.REF_KEY1 = B.ROWNO 
		WHERE B.MEMBERS_ID = p_MEMBERS_ID;
		
		EXCEPTION
			WHEN NO_DATA_FOUND THEN
				v_CUST_ID := NULL;
	END;
  RETURN v_CUST_ID;

END FUN_GET_BE_CUST_ID;
/