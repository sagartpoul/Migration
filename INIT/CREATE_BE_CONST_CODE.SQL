DECLARE
v_ExistCount NUMBER := 0;
BEGIN
DBMS_OUTPUT.PUT_LINE ('....START...creating BE-Constitution Codes.....');
--DROP TABLE IF EXISTS
	SELECT COUNT(*) INTO v_ExistCount FROM user_tables
	WHERE TABLE_NAME = 'BE_CONST_CODE';
	
	IF (v_ExistCount = 1 ) THEN
		EXECUTE IMMEDIATE 'DROP TABLE BE_CONST_CODE';
	END IF;
--STEP-1--START-CREATE TABLE BE_CONST_CODE
EXECUTE IMMEDIATE
'CREATE TABLE  "BE_CONST_CODE" 
   (
	"CONST_CODE" VARCHAR2(1), 
	"TITLE" VARCHAR2(30)
	)';
EXECUTE IMMEDIATE 'CREATE INDEX "BE_CONST_CODE_IDX1" ON "BE_CONST_CODE" ("CONST_CODE")';
DBMS_OUTPUT.PUT_LINE ('....created table BE_CONST_CODE.....');
--STEP-1--END-CREATE TABLE BE_CONST_CODE
DBMS_OUTPUT.PUT_LINE ('....END...creating BE-Constitution Codes.....');
END;
/