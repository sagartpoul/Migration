DECLARE
	v_ExistCount NUMBER := 0;
BEGIN

DBMS_OUTPUT.PUT_LINE ('....START...constructing CSV_MASTER.....');

--DROP TABLE IF EXISTS
	SELECT COUNT(*) INTO v_ExistCount FROM user_tables
	WHERE TABLE_NAME = 'CSV_MASTER';
	
	IF (v_ExistCount = 1 ) THEN
		EXECUTE IMMEDIATE 'DROP TABLE CSV_MASTER';
	END IF;

--STEP-1--START-CREATE TABLE CSV_MASTER
	EXECUTE IMMEDIATE
	'CREATE TABLE  "CSV_MASTER" 
	(	
		"OBJECTID" NUMBER(5,0), 
		"TITLE" VARCHAR2(30), 
		"SRC_TABLE_NAME" VARCHAR2(30)
	)';
	DBMS_OUTPUT.PUT_LINE ('....created table CSV_MASTER.....');
--STEP-1--START-CREATE TABLE CSV_MASTER
END;
